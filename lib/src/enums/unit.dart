/// An enumeration representing different units of time.
enum Unit {
  /// Represents a microsecond.
  microsecond,

  /// Represents a millisecond.
  millisecond,

  /// Represents a second.
  second,

  /// Represents a minute.
  minute,

  /// Represents an hour.
  hour,

  /// Represents a day.
  day,

  /// Represents a week.
  week,

  /// Represents a month.
  month,

  /// Represents a year.
  year,
}
